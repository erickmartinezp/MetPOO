    private void mostrarTodo(){
        String datos = "";
        for (Carrera car : listado){
            datos += car + "\n";
        }
        TfRegistro.setText(datos);
    }

   
    
    private void borrarCampos(){
        TfId.setText("");
        TfNombre.setText ("");
        TfDescripcion.setText ("");
        TfPrecio.setText("");
        
    }    
    private void BtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAgregarActionPerformed
        // TODO add your handling code here:
        Carrera car = new Carrera();
        car.setId(Integer.parseInt (TfId.getText()));
        car.setNombre(TfNombre.getText());
        car.setDescripcion(TfDescripcion.getText());
        car.setPrecio(Double.parseDouble (TfPrecio.getText()));
        listado.add(car);
        JOptionPane.showMessageDialog(this, "Registro guardado...");
        mostrarTodo();
        borrarCampos();
    }//GEN-LAST:event_BtnAgregarActionPerformed

